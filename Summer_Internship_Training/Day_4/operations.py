# Given two numbers entered by user, write expression to print their sum, diff, prod, quotient, floor division, remainder, and the first no. raised to the power of second

a = float(input("Enter the first no. : "))
b = float(input("Enter the second no. : "))

print("sum: ", a+b)
print("dif: ", a-b)
print("prod: ", a*b)
print("quotient: ", a/b)
print("floor division: ", a//b)
print("remainder: ", a%b)
print("first no. raised to the power of second: ", a**b)
